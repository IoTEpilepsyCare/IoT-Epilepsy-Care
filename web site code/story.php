                        <h2>Linnea’s Story</h2> 
                         <img src="images/testimony-img.jpg"
                                                    alt="" width="400 "> 
        
                    </div>
                </div>
            </div>

            
            <p> I was diagnosed with epilepsy in 1999, halfway through my sophomore year of college. I was studying for final exams and writing papers to wrap up the semester. During this time, I had only a few hours of sleep over a three-day period. I finally decided to lie down for a nap.</p> <br/>

<p>I woke up having a seizure. One of my roommates saw it. She was in the room, studying. After I awoke, I asked her if she saw me moving in bed. She said she did but we weren’t sure what happened. It was my first seizure. We decided to call my parents who urged us to go to the E.R. After a brief exam, they sent me home. Still tired, I went back to sleep and again, awoke with some jerking movements. Again, this roommate saw what happened when I was in bed. I returned to the E.R.</p> <br/>

<p>Upon hearing what happened, my mom and dad were on the first flight they could get from Virginia to Ohio. I don’t recall the time period between the phone call and my parents walking into my hospital room.</p> <br/>

<p> After my discharge from the hospital, I returned home and went to a neurologist. I was started on an anti-seizure medication. An EEG was conducted along with an MRI. That data, along with a description of my experience led to the conclusion that I had epilepsy. More specifically, my diagnosis is simple partial seizures with secondary generalized seizures. The history of most of my seizures are nocturnal, occurring around my sleep cycle.</p> <br/>

<p>After final exams were completed and winter break was over, I returned to school. I remember talking with a friend who asked me how I felt about having epilepsy. I had no idea. I remember feeling clueless.</p> <br/>


<p>I had never heard of epilepsy. I didn’t know what to think. I knew I didn’t like what was going on. I didn’t like the seizures, the hospital, the visits to the doctor, and the diagnosis of something permanent in my life that I had never heard of before. Little did I know what traveling this road with this diagnosis would be like.</p>

<p>I tried eight to ten medications over the next six years, none of which fully controlled my seizures. I obtained partial control but that required a cocktail of two to four medications and up to 20 pills a day. As I moved several times, I consulted with several epilepsy centers.</p>

<p>In 2006, I was evaluated for surgery. It took one year. Up until then and through this time, I had an alphabet soup of tests, requiring two hospital stays and many hospital visits. I have had CAT scans, PET scans, MRIs, fMRIs, SPECT scan, EEGs, VEEGs, Ambulatory EEG, MEG, Neuro-psych testing, among others. It was finally determined that I was a candidate for surgery. I was relieved. There was no question in my mind that I wanted to have surgery. I had been on all of the medication options for my type of epilepsy and although I knew other medications were coming down the line, I was losing hope in the success medications would bring in ridding me of seizures and side effects.</p>

<p>Surgery was another source of hope. I talked to many people who had the type of surgery I would be having. My parents and I had done research about surgery and got second opinions, and we felt like we had asked as many questions as we could think of. We researched and talked to different epilepsy centers, doctors, and surgeons, including the mortality rates for this surgery. Though I knew every surgery comes with risks, it also comes with hope.</p>

<p>In 2007, I scheduled a two-stage surgery. The first surgery was seven to eight hours, during which part of my skull was removed and over two hundred electrodes were implanted in my brain in the area that my tests indicated my seizures were coming from. During the week between surgeries I had a VEEG (a video EEG) and was tapered off my medications and sleep deprived to induce seizures. I had over 30 simple partial seizures to give them the information necessary for the second surgery that would also take seven to eight hours. The second surgery included waking me up to further evaluate what tissue could be safely removed, removal of the electrodes, and removal of the epileptic tissue and replacing the piece of skull.</p>

<p>After recovering in the hospital, I experienced 17 beautiful days of seizure freedom. On the 18th day, there was discoloration at the incision site followed by seizures, sickness, tests at my local hospital, and a phone consultation with my surgeon. It was determined that I needed to return to him immediately. My infected skull piece was removed in a four-hour surgery; the infected site was cleaned and my skin was stitched and stapled back together.</p>

<p>I was part of the 3% infection rate from epilepsy surgery at this particular hospital. I got the infection in the hospital. After it was determined what kind of infection it was, I returned home with a PICC line through which I proceeded to give myself antibiotics three times a day for six weeks. I lived without a skull piece for seven months to ensure my body had rid itself from infection. At the end of the seventh month, into 2008, I had a cranioplasty—surgery to implant a prosthetic skull piece.</p>

<p>Currently, I continue to have simple partial seizures several mornings a week, and I’m still on the same cocktail of medicines as pre-surgery. Though I’m not a poster child for a successful surgery, I still believe that surgery is a desirable and effective treatment for some individuals.</p>

<p>And what about the side effects of medicines? Eyes blurring while trying to have a conversation or walk. Losing my train of thought while trying to communicate with someone. Slurring while trying to have a conversation. Anxiety, panic attacks, rashes, depression, ovarian cysts, dizziness, aches, nausea, drugged, dopey, weight gain, weight loss, drowsy. So very tired. Memory. Short term memory—not being able to remember the topic I am discussing with someone or a lesson I have just been taught, or if I have already said, just minutes before, what I am going to say. Long term memory—not being able to recall my epilepsy history, or not having the foggiest memory of stories that my friends tell me I was a part of, not remembering the feeling of living life without epilepsy.</p>

   

